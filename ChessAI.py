def scoreBoard(gs):
    if gs.checkmate:
        if gs.whiteToMove:
            return -CHECKMATE
        else:
            return CHECKMATE

    elif gs.stalemate:
        return 0

    score = 0
    for r in range(len(gs.board)):
        for c in range(len(gs.board[r])):
            square = gs.board[r][c]
            if square[0] == "w":
                score += pieceScore[square[1]] + openingPieceTables[square[1]][r][c]
            elif square[0] == "b":
                score -= pieceScore[square[1]] + openingPieceTables[square[1]][7 - r][c]

    return score


